/* ══════════════════════════════════════════════════════
   SHL Interactive 1: Verify Numerical
   Measures: Numerical reasoning, data interpretation, business numeracy
   Based on: SHL Verify Interactive Numerical Assessment
   ══════════════════════════════════════════════════════ */
class VerifyNumericalGame {
  constructor(container, cb) {
    this.container = container; this.cb = cb;
    this.score = 0; this.correct = 0; this.total = 0;
    this.qIndex = 0;
    this.el = null;
    this._timers = [];
    this.hasAnswered = false;

    this.questions = [
      {
        title: "Global Sales Performance",
        subtitle: "Sales revenue by division (in thousands USD)",
        headers: ["Division", "Q1 Sales ($k)", "Q2 Sales ($k)"],
        rows: [
          ["Division A", "120", "150"],
          ["Division B", "80", "100"],
          ["Division C", "200", "180"]
        ],
        text: "Which division(s) achieved the highest percentage increase in sales from Q1 to Q2?",
        options: [
          "Division A only (25%)",
          "Division B only (25%)",
          "Both Division A and B (Both increased by 25%)",
          "Division C only"
        ],
        answer: 2, // Index of correct option (Both Division A and B)
        explanation: "Division A grew by (150-120)/120 = 25%. Division B grew by (100-80)/80 = 25%. Both have the same highest growth rate."
      },
      {
        title: "Subscription Plans & Churn (May)",
        subtitle: "Active users and monthly churn rates",
        headers: ["Plan", "Active Users", "Churn Rate (%)"],
        rows: [
          ["Basic", "10,000", "5.0%"],
          ["Pro", "4,000", "2.0%"],
          ["Enterprise", "500", "4.0%"]
        ],
        text: "What is the total number of users who churned across all plans during May?",
        options: [
          "580 users",
          "600 users",
          "620 users",
          "650 users"
        ],
        answer: 1, // 600 users
        explanation: "Basic churn: 10,000 * 0.05 = 500. Pro churn: 4,000 * 0.02 = 80. Enterprise churn: 500 * 0.04 = 20. Total: 500 + 80 + 20 = 600."
      },
      {
        title: "Marketing Spend vs Customer Acquisition",
        subtitle: "Quarterly marketing spend and customer acquisitions",
        headers: ["Quarter", "Spend ($)", "New Customers"],
        rows: [
          ["Q1", "50,000", "2,500"],
          ["Q2", "80,000", "3,200"],
          ["Q3", "60,000", "2,400"]
        ],
        text: "In which quarter was the Customer Acquisition Cost (Spend / New Customers) the lowest?",
        options: [
          "Q1",
          "Q2",
          "Q3",
          "The cost was identical in all quarters"
        ],
        answer: 0, // Q1 ($20 vs $25)
        explanation: "Q1 CAC = $50,000 / 2,500 = $20. Q2 CAC = $80,000 / 3,200 = $25. Q3 CAC = $60,000 / 2,400 = $25. Q1 had the lowest CAC."
      },
      {
        title: "E-Commerce Revenue Distribution",
        subtitle: "Revenue split by traffic source (USD)",
        headers: ["Source", "Revenue ($)"],
        rows: [
          ["Direct", "140,000"],
          ["Referral", "60,000"],
          ["Social", "40,000"],
          ["Search", "160,000"]
        ],
        text: "What percentage of the total e-commerce revenue was generated by Search traffic?",
        options: [
          "35%",
          "40%",
          "45%",
          "50%"
        ],
        answer: 1, // 40%
        explanation: "Total revenue = 140,000 + 60,000 + 40,000 + 160,000 = 400,000. Search share = 160,000 / 400,000 = 40%."
      }
    ];
  }

  start() {
    this.el = document.createElement('div');
    this.el.className = 'shl-num-game';
    this.container.appendChild(this.el);
    this._showQuestion();
  }

  _showQuestion() {
    if (this.qIndex >= this.questions.length) {
      this._finish();
      return;
    }

    this.hasAnswered = false;
    const q = this.questions[this.qIndex];

    this._render(q);
  }

  _render(q) {
    if (!this.el) return;

    this.el.innerHTML = `
      <div class="shl-header">
        <div class="shl-stat"><span class="shl-l">Question</span><span class="shl-v" style="color:var(--violet-l)">${this.qIndex + 1}/${this.questions.length}</span></div>
        <div class="shl-stat"><span class="shl-l">Score</span><span class="shl-v" style="color:var(--amber)">${this.score}</span></div>
      </div>
      <div class="prog-bar"><div class="prog-fill" style="width:${(this.qIndex) / this.questions.length * 100}%"></div></div>
      
      <div class="shl-workspace">
        <div class="shl-data-panel">
          <div class="shl-table-card">
            <h3>📈 ${q.title}</h3>
            <p class="shl-table-subtitle">${q.subtitle}</p>
            <table class="shl-data-table">
              <thead>
                <tr>
                  ${q.headers.map(h => `<th>${h}</th>`).join('')}
                </tr>
              </thead>
              <tbody>
                ${q.rows.map(row => `
                  <tr>
                    ${row.map(val => `<td>${val}</td>`).join('')}
                  </tr>
                `).join('')}
              </tbody>
            </table>
          </div>
        </div>
        
        <div class="shl-question-panel">
          <div class="shl-question-text">${q.text}</div>
          <div class="shl-options-list" id="shl-options">
            ${q.options.map((opt, idx) => `
              <button class="btn shl-opt-btn" data-idx="${idx}">
                <span class="shl-opt-letter">${String.fromCharCode(65 + idx)}</span>
                <span class="shl-opt-text">${opt}</span>
              </button>
            `).join('')}
          </div>
          <div class="shl-explanation" id="shl-explanation" style="display:none"></div>
        </div>
      </div>
      
      <div class="shl-footer" style="display:none" id="shl-next-panel">
        <button class="btn btn-primary" id="shl-next-btn">Next Question ➔</button>
      </div>`;

    this.el.querySelectorAll('.shl-opt-btn').forEach(btn => {
      btn.addEventListener('click', () => {
        const idx = parseInt(btn.dataset.idx);
        this._pickAnswer(idx);
      });
    });
  }

  _pickAnswer(choiceIdx) {
    if (this.hasAnswered) return;
    this.hasAnswered = true;
    this.total++;

    const q = this.questions[this.qIndex];
    const isCorrect = (choiceIdx === q.answer);
    
    // Highlight buttons
    const btns = this.el.querySelectorAll('.shl-opt-btn');
    btns.forEach(btn => {
      const idx = parseInt(btn.dataset.idx);
      if (idx === q.answer) {
        btn.classList.add('shl-opt-correct');
      } else if (idx === choiceIdx && !isCorrect) {
        btn.classList.add('shl-opt-wrong');
      }
    });

    if (isCorrect) {
      this.correct++;
      const pts = 250;
      this.score += pts;
      this.cb.onScore(pts, this.qIndex + 1);
      this.cb.onFeedback(true);
    } else {
      this.cb.onFeedback(false);
    }

    // Show explanation
    const expEl = this.el.querySelector('#shl-explanation');
    if (expEl) {
      expEl.style.display = 'block';
      expEl.innerHTML = `<strong>${isCorrect ? 'Correct!' : 'Incorrect.'}</strong> ${q.explanation}`;
    }

    // Show next button
    const nextPanel = this.el.querySelector('#shl-next-panel');
    if (nextPanel) {
      nextPanel.style.display = 'flex';
      const nextBtn = this.el.querySelector('#shl-next-btn');
      if (nextBtn) nextBtn.addEventListener('click', () => {
        this.qIndex++;
        this._showQuestion();
      });
    }
  }

  _finish() {
    this.cb.onEnd({
      score: this.score,
      accuracy: this.total ? (this.correct / this.total) * 100 : 0,
      avgTime: 0,
      correct: this.correct,
      total: this.total,
      level: 1
    });
  }

  timeUp() { this._finish(); }
  destroy() {
    this._timers.forEach(clearTimeout);
    this.el = null;
  }
}
window.VerifyNumericalGame = VerifyNumericalGame;
